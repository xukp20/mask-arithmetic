class DynamicEmbedding(nn.Module):
    """
        Store the dict of {id: embedding: Parameter} for dynamic embedding
    """
    def __init__(
        self,
        vocab_size: int,
        embed_dim: int,
        padding_idx: int,
    ):
        super().__init__()
        self.vocab_size = vocab_size
        self.embed_dim = embed_dim
        self.padding_idx = padding_idx
        self.embeddings = nn.ParameterDict()

        self._init_weights()
    
    def _init_weights(self):
        # add self.vocab_size embeddings, except for padding_idx
        vector_size = self.embed_dim
        for i in range(self.vocab_size):
            if i != self.padding_idx:
                self.embeddings[i] = nn.Parameter(torch.normal(0, 1, (vector_size,)))
            else:
                self.embeddings[i] = nn.Parameter(torch.zeros(vector_size))
                self.embeddings[i].requires_grad = False

    def forward(self, input_ids):
        # [bs, seq_len] -> [bs, seq_len, embed_dim]
        return self.embeddings[input_ids]
    
    def add_embedding(self, id=None, embedding=None):
        if not id:
            id = len(self.embeddings)
        if not embedding:
            embedding = torch.normal(0, 1, (self.embed_dim,))
        self.embeddings[id] = nn.Parameter(embedding)
        self.vocab_size += 1


class LlamaModel(LlamaPreTrainedModel):
    """
    Transformer decoder consisting of *config.num_hidden_layers* layers. Each layer is a [`LlamaDecoderLayer`]

    Args:
        config: LlamaConfig
    """

    def __init__(self, config: LlamaConfig):
        super().__init__(config)
        self.padding_idx = config.pad_token_id
        self.vocab_size = config.vocab_size

        # self.embed_tokens = nn.Embedding(config.vocab_size, config.hidden_size, self.padding_idx)
        # self.embed_tokens = DynamicEmbedding(config.vocab_size, config.hidden_size, self.padding_idx)

        self.layers = nn.ModuleList(
            [LlamaDecoderLayer(config, layer_idx) for layer_idx in range(config.num_hidden_layers)]
        )
        self.norm = LlamaRMSNorm(config.hidden_size, eps=config.rms_norm_eps)
        self.gradient_checkpointing = False

        # Initialize weights and apply final processing
        self.post_init()

    def forward(
        self,
        # input_ids: torch.LongTensor = None,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
    ) -> Union[Tuple, BaseModelOutput]:
        output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
        output_hidden_states = (
            output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
        )
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        # if (input_ids is None) ^ (inputs_embeds is not None):
        #     raise ValueError(
        #         "You cannot specify both input_ids and inputs_embeds at the same time, and must specify either one"
        #     )

        # if inputs_embeds is None:
        #     inputs_embeds = self.embed_tokens(input_ids)

        if position_ids is None:
            position_ids = torch.arange(inputs_embeds.size(1), device=inputs_embeds.device, dtype=torch.long).unsqueeze(0)

        attn_mask = self._update_attn_mask(attention_mask, inputs_embeds)

        # embed positions
        hidden_states = inputs_embeds

        # decoder layers
        all_hidden_states = () if output_hidden_states else None
        all_self_attns = () if output_attentions else None

        for decoder_layer in self.layers:
                
            if output_hidden_states:
                all_hidden_states += (hidden_states,)

            if self.gradient_checkpointing and self.training:
                layer_outputs = self._gradient_checkpointing_func(
                    decoder_layer.__call__,
                    hidden_states,
                    attn_mask,
                    position_ids,
                    output_attentions,
                )
            else:
                layer_outputs = decoder_layer(
                    hidden_states,
                    attention_mask=attn_mask,
                    position_ids=position_ids,
                    output_attentions=output_attentions,
                )

            hidden_states = layer_outputs[0]

            if output_attentions:
                all_self_attns += (layer_outputs[1],)

        hidden_states = self.norm(hidden_states)

        # add hidden states from the last decoder layer
        if output_hidden_states:
            all_hidden_states += (hidden_states,)

        if not return_dict:
            return tuple(v for v in [hidden_states, all_hidden_states, all_self_attns] if v is not None)
        return BaseModelOutput(
            last_hidden_state=hidden_states,
            hidden_states=all_hidden_states,
            attentions=all_self_attns,
        )

    # for bi-directional attention, only consider the fa2 implementation
    def _update_attn_mask(self, attention_mask, input_embeds):
        dtype = input_embeds.dtype
        if self.config._attn_implementation == "flash_attention_2":
            return attention_mask   # 2d masks
        # for eager and sdpa, create attn scores mask of 4d, [bs, 1, seq_len, seq_len]
        else:
            # from [bs, seq_len] to [bs, 1, seq_len, seq_len]
            # [bs, seq_len, 1] * [bs, 1, seq_len] -> [bs, seq_len, seq_len]
            attention_mask = attention_mask.unsqueeze(2) * attention_mask.unsqueeze(1)
            # map to float
            inverted_mask = attention_mask == 0
            attention_mask = torch.zeros(attention_mask.shape, dtype=dtype, device=attention_mask.device).masked_fill(inverted_mask, -1e9)
            return attention_mask.unsqueeze(1).to(dtype)


IGNORE_INDEX=-100

class L2_Loss(nn.Module):
    def __init__(self):
        super(L2_Loss, self).__init__()

    def forward(self, hidden_states, labels, label_embeddings):
        # use l2 loss
        # flatten and drop the ignored index
        flatten_hidden_states = hidden_states.view(-1, hidden_states.size(-1))
        label_embeddings = label_embeddings.view(-1, label_embeddings.size(-1))
        flatten_labels = labels.view(-1)
        kept_indices = flatten_labels != IGNORE_INDEX
        flatten_hidden_states = flatten_hidden_states[kept_indices]
        flatten_label_embeddings = label_embeddings[kept_indices]
        
        # calculate the l2 loss
        loss_fct = nn.MSELoss()
        loss = loss_fct(flatten_hidden_states, flatten_label_embeddings)
        return loss


class Contrastive_L2_Loss(nn.Module):
    """
        Contrastive L2 loss: distance between matched hidden and label embedding - distances between mismatched hidden and label embedding
    """
    def __init__(self, neg_count=1):
        super(Contrastive_L2_Loss, self).__init__()
        self.neg_count = neg_count

    def forward(self, hidden_states, labels, label_embeddings):
        # hidden_states: [bs, seq_len, hidden_size]
        # labels: [bs, seq_len]
        # label_embeddings: [bs, seq_len, hidden_size]

        flatten_hidden_states = hidden_states.view(-1, hidden_states.size(-1))
        label_embeddings = label_embeddings.view(-1, label_embeddings.size(-1))
        flatten_labels = labels.view(-1)
        kept_indices = flatten_labels != IGNORE_INDEX
        flatten_hidden_states = flatten_hidden_states[kept_indices]
        flatten_label_embeddings = label_embeddings[kept_indices]
        flatten_labels = flatten_labels[kept_indices]

        # for each match, choose one mismatch label_embedding that is from a different label id 
        # and calculate the contrastive loss
        # 1. take all positions that are not the same as the label at the position
        different_ids = flatten_labels != flatten_labels.unsqueeze(1)  # [bs*seq_len, bs*seq_len]
        # pick out neg_count different label embeddings for each position
        # sample out neg count for each row, where different ids is True

        def get_neg_ids(different_ids, neg_count):
            neg_ids = []
            for i in range(different_ids.size(0)):
                neg_id = torch.where(different_ids[i] == 1)[0]
                neg_id = neg_id[torch.randperm(neg_id.size(0))[:neg_count]]
                neg_ids.append(neg_id)
            return torch.stack(neg_ids, dim=0)

        neg_ids = get_neg_ids(different_ids, self.neg_count)    # [bs*seq_len, neg_count]
        # get the negative label embeddings
        neg_label_embeddings = flatten_label_embeddings[neg_ids]  # [bs*seq_len, neg_count, hidden_size]

        # calculate the match l2 distance 
        loss_fct = nn.MSELoss()
        match_l2 = loss_fct(flatten_hidden_states, flatten_label_embeddings)

        # calculate the mismatch l2 distance
        neg_label_embeddings = neg_label_embeddings.view(-1, neg_label_embeddings.size(-1))
        expanded_hidden_states = flatten_hidden_states.unsqueeze(1).expand(-1, self.neg_count, -1).contiguous().view(-1, flatten_hidden_states.size(-1))
        mismatch_l2 = loss_fct(expanded_hidden_states, neg_label_embeddings)
        loss = match_l2 - mismatch_l2

        return loss, match_l2, mismatch_l2


class InfoNCE_Loss(nn.Module):
    def __init__(self):
        super(InfoNCE_Loss, self).__init__()

    def forward(self, hidden_states, labels, word_embeddings):
        # hidden_states: [bs, seq_len, hidden_size]
        # labels: [bs, seq_len]
        # word_embeddings: [vocab_size, hidden_size]

        # compute logits by mat mul of hidden_states and word_embeddings
        logits = torch.matmul(hidden_states, word_embeddings.T) # [bs, seq_len, vocab_size]

        # shift logits and labels
        shift_logits = logits[..., :-1, :].contiguous()
        shift_labels = labels[..., 1:].contiguous()
        # flatten the tokens
        shift_logits = shift_logits.view(-1, shift_logits.size(-1))
        shift_labels = shift_labels.view(-1).to(shift_logits.device)

        # compute the loss
        loss_fct = nn.CrossEntropyLoss()
        loss = loss_fct(shift_logits, shift_labels)

        return loss


